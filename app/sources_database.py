def get_sources_for_industry(industry: str) -> list:
    """
    Get curated sources for a specific industry.
    
    Args:
        industry (str): Industry name (hotel, vacation_rental, restaurant, fitness, salon, fahrschule, immobilien, other)
        
    Returns:
        list: List of source dictionaries with title, url, description, and key_insight
    """
    # Normalize industry name
    industry = industry.lower().strip()
    
    # Return industry-specific sources or default to 'other'
    return SOURCES.get(industry, SOURCES.get("other", []))


def format_sources_for_pdf(sources: list) -> str:
    """
    Format sources for PDF report display.
    Returns numbered sources with title, URL, and key insight.
    """
    if not sources:
        return "No sources available for this industry."
    
    formatted = []
    for idx, source in enumerate(sources, 1):
        formatted.append(f"[{idx}] {source['title']}")
        formatted.append(f"    {source['url']}")
        formatted.append(f"    Key Insight: {source['key_insight']}\n")
    
    return "\n".join(formatted)


def format_sources_for_prompt(sources: list) -> str:
    """
    Format sources for AI prompt context.
    Provides industry research context for analysis.
    """
    if not sources:
        return "No specific industry sources available for this analysis."
    
    formatted = ["INDUSTRY RESEARCH SOURCES:"]
    formatted.append("Use these authoritative sources to inform your analysis:\n")
    
    for idx, source in enumerate(sources, 1):
        formatted.append(f"{idx}. {source['title']}")
        formatted.append(f"   URL: {source['url']}")
        formatted.append(f"   Context: {source['description']}")
        formatted.append(f"   Key Insight: {source['key_insight']}\n")
    
    return "\n".join(formatted)


